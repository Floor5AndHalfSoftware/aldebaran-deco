  },
  created() {
    this.getFaq();
  },
  methods: {
    getFaq() {
      this.loading = true;
      let formData = new FormData();
      formData.append("language", store.state.locate);
      formData.append("group", "faq");
      formData.append("status", 1);
      this.$http
        .post("cms/text/list", formData)
        .then((response) => {
          let result = response.data;
          this.forceLogout(result.result);
          if (result.result == 0) {
            let faqs = result.contents;
            faqs.forEach((e) => {
              if (e.category_id == "faq general") this.faqGeneral.push(e);
              if (e.category_id == "faq casino") this.faqCasino.push(e);
              if (e.category_id == "faq bets") this.faqBets.push(e);
              if (e.category_id == "faq accounts") this.faqAccounts.push(e);
              if (e.category_id == "faq deposits") this.faqDeposits.push(e);
            });
          } else {
            this.alert = { type: "alert-danger", message: result.description };
          }
        })
        .catch((response) => {
          this.showError(response);
        })
        .finally(() => {
          this.loading = false;
        });
    },
    sendEmail() {
      this.alert1 = { type: "alert-success", message: "" };
      if (!this.email || !this.name || !this.comments) {
        this.alert1 = {
          type: "alert-danger",
          message: this.getLocate("send-validate"),
        };
        return;
      }
      this.loading1 = true;
      let formData = new FormData();
      formData.append("name", this.name);
      formData.append("email", this.email);
      formData.append("comments", this.comments);
      this.$http
        .post("sendContactUs", formData)
        .then((response) => {
          let result = response.data;
          if (result.message == "OK") {
            this.alert1 = {
              type: "alert-success",
              message: this.getLocate("send-ok"),
            };
            this.name = null;
            this.email = null;
            this.comments = null;
          } else {
